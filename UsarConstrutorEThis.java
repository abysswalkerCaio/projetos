package projetos;

public class UsarConstrutorEThis {
	String nome, genero;
	int idade;
	double altura;

	// Criando um construtor:
	public UsarConstrutorEThis(String nome, String genero, int idade, double altura) {
		this.nome = nome;
		this.genero = genero;
		this.idade = idade;
		this.altura = altura;
	}
}
